export async function createPatient(input: z.infer<typeof createPatientSchema>) {
  const profile = await getCurrentProfile();
  if (!profile?.tenant) {
    return { ok: false as const, error: 'No autorizado' };
  }

  const parsed = createPatientSchema.safeParse(input);
  if (!parsed.success) {
    const firstError = parsed.error.issues[0];
    return {
      ok: false as const,
      error: firstError?.message || 'Datos invalidos',
    };
  }

  const supabase = await createServerSupabase();
  const data = parsed.data;

  const newPatient = {
    tenant_id: profile.tenant.id,
    first_name: data.first_name.trim(),
    last_name: data.last_name.trim(),
    email: cleanString(data.email),
    phone: cleanString(data.phone),
    phone_secondary: cleanString(data.phone_secondary),
    date_of_birth: cleanString(data.date_of_birth),
    gender: cleanString(data.gender),
    address: cleanString(data.address),
    city: cleanString(data.city),
    document_type: cleanString(data.document_type),
    document_number: cleanString(data.document_number),
    emergency_contact_name: cleanString(data.emergency_contact_name),
    emergency_contact_phone: cleanString(data.emergency_contact_phone),
    blood_type: cleanString(data.blood_type),
    allergies: cleanString(data.allergies),
    medical_notes: cleanString(data.medical_notes),
    occupation: cleanString(data.occupation),
    insurance_provider: cleanString(data.insurance_provider),
    insurance_number: cleanString(data.insurance_number),
    responsible_name: cleanString(data.responsible_name),
    responsible_phone: cleanString(data.responsible_phone),
    responsible_relationship: cleanString(data.responsible_relationship),
    source: cleanString(data.source),
    is_active: true,
  };

  const { data: created, error } = await supabase
    .from('patients')
    .insert(newPatient)
    .select('id')
    .single();

  if (error || !created) {
    return { ok: false as const, error: 'Error al crear paciente' };
  }

  revalidatePath('/dental/patients');
  revalidatePath('/dental/dashboard');

  return { ok: true as const, id: created.id };
}